package org.example.entities.types;

/**
 * Represents the vertical directions.
 * The available directions are UP, DOWN, and NONE.
 */
public enum VerticalDirectionType {
    /**
     * Represents the upward direction.
     */
    UP,

    /**
     * Represents the downward direction.
     */
    DOWN,

    /**
     * Represents no vertical direction.
     */
    NONE
}
